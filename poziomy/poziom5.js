function rysuj() {
  ctx.strokeStyle = "rgba(255, 255, 255, 0.15)";
  ctx.lineWidth = 1;

  for (let row = 0; row < plotno.length; row++) {
    for (let col = 0; col < plotno[row].length; col++) {
      if (plotno[row][col] == 1) {
        ctx.drawImage(bloczek, col * size, row * size, size, size);
      } else {
        ctx.fillStyle = "black";
        ctx.fillRect(col * size, row * size, size, size);
      }
      ctx.strokeRect(col * size, row * size, size, size);
    }
  }
  const monsterSize = 90;
potwory.forEach(m => {
   
    let offset = (size - monsterSize) / 2;
    
    ctx.drawImage(
      potwor, 
      m.col * size + offset, 
      m.row * size + offset, 
      monsterSize, 
      monsterSize
    );
  });
}
function updateMonsters() {
  potwory.forEach(m => {
    if (m.typ === "gora-dol") {
      m.row += m.dir * m.speed;

    
      if (m.row >= m.endRow) {
        m.row = m.endRow;
        m.dir = -1; 
      } else if (m.row <= m.startRow) {
        m.row = m.startRow;
        m.dir = 1;  
      }
    }
  else if (m.typ == "prawo-lewo") {
      m.col += m.dir * m.speed;

      if (m.col >= m.endCol) {
        m.col = m.endCol;
        m.dir = -1; // w lewo
      } else if (m.col <= m.startCol) {
        m.col = m.startCol;
        m.dir = 1;  // w prawo
      }
    }
  });
}


function gameLoop() {
  updateMonsters(); 
  ctx.clearRect(0, 0, area.width, area.height);
  rysuj(); 
  requestAnimationFrame(gameLoop); 
}

// Uruchomienie gdy obrazki będą gotowe
let loadedImages = 0;
function checkImagesLoaded() {
  loadedImages++;
  if (loadedImages === 2) {
    gameLoop();
  }
}

bloczek.onload = checkImagesLoaded;
